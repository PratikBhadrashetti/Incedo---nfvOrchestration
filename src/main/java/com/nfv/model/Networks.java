package com.nfv.model;

public class Networks {

	private String uuid = "7e97f895-cf02-4ca3-97cd-7745e0496a0f";
	
	private String id;
	
	private String name;
	
	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
