package com.nfv.model;

public class Stack {

	private String id;
	
	private String stack_status;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStack_status() {
		return stack_status;
	}

	public void setStack_status(String stack_status) {
		this.stack_status = stack_status;
	}
}
